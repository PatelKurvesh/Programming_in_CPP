/*Relational Operator 
==
<
>
<=
>=
!=
*/

#include<iostream>
using namespace std;

int main()
{
    int a=6;
    int b=7;

    int first= (a==b);
    cout << first << endl;
    
    int Second= (a<b);
    cout << Second << endl;
    
    int Third= (a>b);
    cout << Third << endl;
    
    int Four= (a<=b);
    cout << Four << endl;
    
    int Fifth= (a>=b);
    cout << Fifth << endl;
    
    int Six= (a!=b);
    cout << Six << endl;
    
}